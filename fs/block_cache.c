// Return the virtual address of this disk block.
void *
diskaddr(uint32_t blockno)
{
	if (blockno == 0 || (super && blockno >= super->s_nblocks))
		panic("bad block number %08x in diskaddr", blockno);

	return (char *)DISKMAP + blockno * BLKSIZE;
}

